#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*new_s;
	size_t	i;

	len = ft_strlen(s);
	new_s = malloc(sizeof(char) * (len + 1));
	if (new_s == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		new_s[i] = s[i];
		i++;
	}
	new_s[i] = '\0';
	return (new_s);
}